public class MilesMorales implements Ataques {


    public void Golpes() {
        System.out.println("le asesta al villano una rafaga de golpes");
    }


    public void Patadas() {
        System.out.println("le da una patada en las costillas, pero el villano no se inmuta");
    }


    public void SpiderSense() {
        System.out.println("el villano se abalanza sobre el y el sentido de peligro lo hace que lo esquive dando" +
                " un gran salto");
    }


    public void ContraAtaque() {
        System.out.println("al aterrizar se hace invisible y corre hacia el dandole un fuerte golpe que lo derribe");
    }


    public void SpiderWeb() {
        System.out.println("aparece de nuevo y le inmoviliza las piernas  con su spider web");
    }


    public void Finisher() {
        System.out.println("le da un golpe fulminante y lo cuelga de una lampara");
    }
}
