package semana1.Diego.Tareas.Spiderverse;

public class SpiderHam implements Ataques{


    public void Golpes() {
        System.out.println("da un gancho a el rostro del villano");
    }


    public void Patadas() {
        System.out.println("hace una patada baja giratoria para derribar a el villano");
    }


    public void SpiderSense() {
        System.out.println("al arrojarse sobre el presiente peligro, el villano tiene una navaja");
    }


    public void ContraAtaque() {
        System.out.println("el villano le lanza ataques por todos lados pero SpiderHam las esquiva y le da una " +
                "patada al rostro");
    }


    public void SpiderWeb() {
        System.out.println("usa su spider web para arrebatarle la navaja");

    }


    public void Finisher() {
        System.out.println("brinca y saca su mazo gigante para aplastarlo, al final solo lo inmoviliza con su spider web");
    }
}
